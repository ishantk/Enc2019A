
public class SingleValueContainers {

	// Whatever we write in main gets executed
	public static void main(String[] args) {
		
		// How to create a Storage Container ?
		byte age = 10;
		// age is the name of storage container
		// byte is type of container which also is size i.e. 8 bits here
		// 10 is value i.e. data
		
		System.out.println("Age is: "+age);
		
		// Updating data in container called age
		age = 18;
		
		// Read Operation
		System.out.println("Age now is: "+age);
		
		double salary = 345611.34;
		
		char ch = 'A';
		ch = 66;
		ch = '\u20b9';
		System.out.println("ch is: "+ch);
		
		boolean isInternetConnected = true;
		isInternetConnected = false;

	}

}
